package org.dante.demo.amqp.dto.req;

public class Data {
	
	private String name;
	private int age;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	@Override
	public String toString() {
		return "Data [name=" + name + ", age=" + age + "]";
	}
	
}
